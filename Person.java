class Person {
  
  public String id;
  public String fName;
  public String mNames;
  public String lName;
  public String sex;
  public int age;
  
  public Person(String id, String fName, String mNames, String lName, String sex, int age) {
    this.id =id;
    this.fName = fName;
    this.mNames = mNames;
    this.lName = lName;
    this.sex = sex;
    this.age = age;
  } // end Person
} // end class Person